    private void InicioSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InicioSesionActionPerformed
        Usuario.getText();
        if (empleado.findEmpleados(Integer.parseInt(Usuario.getText()))!=null) {
            if (responsable.findResponsables(Integer.parseInt(Usuario.getText()))!=null) {
                Responsable.setSize(445,530);
                Responsable.setVisible(true);
            }else if (director.findDirectores(Integer.parseInt(Usuario.getText()))!=null) {
                Director.setSize(477,494);
                Director.setVisible(true);
            }else if (jefe.findJefesArea(Integer.parseInt(Usuario.getText()))!=null) {
                JefeArea.setSize(513, 598);
                JefeArea.setVisible(true);
            }else{
                ErrorInicio.setText("Usuario incorrecto");
            }
        }
    }//GEN-LAST:event_InicioSesionActionPerformed

